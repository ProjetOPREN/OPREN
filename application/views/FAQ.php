	<div id="karen"><h3>Karen, c'est quoi ?</h3></div>
	<p>Karen est un site de covoiturage gratuit destiné aux personnes souhaitant aller à l'Open de tennis de Rennes sans se soucier de prendre la voiture ou en faisant un geste pour l'environnement. Vous pouvez y proposer ou réserver des covoiturages.</p>
	<div id="création-compte"><h3>Dois-je me créer un compte pour proposer ou réserver un covoiturage ?</h3></div>
	<p>Il est nécessaire de se créer un compte pour utiliser le site. Pour vous créer un compte, il suffit de cliquer sur l'onglet "Connexion" puis de vous inscrire. Il ne vous reste plus qu'à remplir les champs demandés et de valider. Un mail de validation de création de compte vous sera envoyé rapidement.</p>
	<p>Créer un compte est entièrement gratuit.</p>
	<p>Vous pouvez aussi vous connecter via Facebook à partir de la page de connexion.</p>
	<div id="mdp"><h3>J'ai oublié mon mot de passe, comment faire ?</h3></div>
	<p>Vous pouvez réinitialiser votre mot de passe à partir de la page de connexion. Un nouveau mot de passe vous sera envoyé par mail.</p>
	<div id="modif"><h3>Comment modifier mes informations ou mon mot de passe ?</h3></div>
	<p> Après vous être connecté, allez dans l'onglet "Mon Compte" puis cliquez sur "Modifier". Remplissez les champs que vous souhaitez changer puis validez.</p>
	<div id="info"><h3>Mes informations sont-elles affichées sur le site ?</h3></div>
	<p>Seuls votre nom et prénom seront affichés publiquement. Votre numéro de téléphone sera visible uniquement pour les personnes ayant réservé votre covoiturage ou dont vous avez réservé une place. Votre adresse e-mail n'est jamais visible.</p>
	<p>Aucunes informations n'est utilisées à des fins commerciales.</p>
	<div id="gratification"><h3>Y a-t-il une gratification pour proposer et amener des personnes ?</h3></div>
	<p>Aucune gratification n'est prévu pour l'instant.</p>
	<div id="payer"><h3>Dois-je payer quelque chose pour réserver un covoiturage ?</h3></div>
	<p>Non, les covoiturages sont entièrement gratuits.</p>
	<div id="proposer"><h3>Comment faire pour proposer un covoiturage ?</h3></div>
	<p>À partir de la page d'accueil, cliquer sur "J'offre un covoiturage". Remplissez les champs nécessaires et cliquez sur "Envoyer l'offre". Votre covoiturage est maintenant visible et réservable.</p>
	<p>Lorqu'une personne réserve votre covoiturage, un mail vous sera envoyé pour vous prévenir de la réservation et contenant son numéro de téléphone.</p>
	<div id="reserver"><h3>Comment faire pour réserver un covoiturage ?</h3></div>
	<p>À partir de la page d'accueil, cliquez sur "Je recherche un covoiturage", sélectionnez la date souhaitée puis validez. Vous obtiendrez la liste des covoiturages disponibles ce jour là.</p>
	<p>si vous réservez un covoiturage, un mail contentant les informations du conducteur vous sera envoyé.</p>
	<p>Si aucun covoiturage ne vous convient ou s'il n'y en a pas le jour souhaité, vous pouvez créer une alerte en cliquant sur le bouton correspondant. Vous recevrez automatiquement un mail pour vous prévenir qu'un nouveau covoiturage a été proposé pour ce jour.</p>
	<div id="annuler"><h3>Je souhaite annuler un covoiturage ou une réservation, comment faire ?</h3></div>
	<p>Vous pouvez annuler votre covoiturage à partir de la page "Mes covoiturages". De même vous pouvez annuler une réservation à partir de la page "Mes Réservations". Dans les deux cas, un mail sera envoyé pour prévenir les personnes concernées.</p>
	<div id="smartphone"><h3>Puis-je utiliser le site à partir de mon smartphone ?</h3></div>
	<p>Oui, vous pouvez consulter le site à partir de n'importe quel navigateur Internet et à partir de n'importe quel support.</p>
	<div id="probleme"><h3>Un problème est arrivée lors du covoiturage, quel recourt ai-je ?</h3></div>
	<p>Karen ne peut être tenu responsable en cas de litige lors des trajets organisés sur le site, ni lors de n'importe quel utilisation du site.</p>
	<div id="contacter"><h3>J'ai une question ou une remarque, comment faire pour vous la communiquer ?</h3></div>
	<p>Vous pouvez nous écrire directement à partir de la page "Contact".</p>
